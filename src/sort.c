#include "../inc/push_swap.h"

int is_array_sorted(t_stacks *s)
{
    int i;
    i = 0;
    while(i < s->a_size - 1)
    {
        if(s->a[i] > s->a[i + 1])
            return (0);
        i++;
    }
    return (1);
}